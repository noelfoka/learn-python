# Add the item to the end of the list
myList = ["apple", "banana", "cherry"]
myList.append("Orange")
print(myList)

# Insert an item as the second position
myList.insert(1, "watermelon")
print(myList)

# Extend lists
thisList = [
    "potato",
    "igname",
    "cocoyam",
]

myList.extend(thisList)
print(myList)
